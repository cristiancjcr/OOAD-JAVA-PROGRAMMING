public class C2Nr {
    private double num1;
    private double num2;

    // Constructor care primește două numere
    public C2Nr(double num1, double num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    // Metodă pentru calcularea sumei celor două numere
    public double suma() {
        return num1 + num2;
    }
}
