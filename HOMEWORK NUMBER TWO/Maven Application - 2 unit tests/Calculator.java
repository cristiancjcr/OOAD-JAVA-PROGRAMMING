package com.example;

/**
 * Clasa Calculator oferă metode pentru operații matematice simple.
 */
public class Calculator {

    /**
     * Adună două numere întregi.
     * @param a Primul număr
     * @param b Al doilea număr
     * @return Suma lui a și b
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Înmulțește două numere întregi.
     * @param a Primul număr
     * @param b Al doilea număr
     * @return Produsul lui a și b
     */
    public int multiply(int a, int b) {
        return a * b;
    }
}
